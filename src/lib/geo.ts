export function getRandomPhotoLocation(excludeIds: string[] = []): PhotoLocation {
  const validLocations = PHOTO_LOCATIONS.filter(loc => !excludeIds.includes(loc.id));
  const pool = validLocations.length > 0 ? validLocations : PHOTO_LOCATIONS; // Fallback if all shown
  const idx = Math.floor(Math.random() * pool.length);
  return pool[idx];
}

// Returns distance in kilometers using Haversine formula
export function calculateDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a)); 
  const distance = R * c; 
  return distance;
}

// Calculate score based on distance (max 5000)
// Using an exponential decay so that scores drop off reasonably
export function calculateScore(distanceKm: number): number {
  if (distanceKm <= 0.05) return 5000; 
  
  // Magic constant to scale the difficulty
  const scale = 2000;
  let score = 5000 * Math.exp(-distanceKm / scale);
  
  return Math.max(0, Math.round(score));
}
